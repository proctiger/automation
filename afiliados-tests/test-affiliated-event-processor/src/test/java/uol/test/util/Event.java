package uol.test.util;

public class Event {

    private int product;

    private String desName;

    private String caf;

    private int numEventAmount;

    private double numValue;

    private String desGrouping;

    public String getDesName() {
        return desName;
    }

    public void setDesName(String desName) {
        this.desName = desName;
    }

    public String getCaf() {
        return caf;
    }

    public void setCaf(String caf) {
        this.caf = caf;
    }

    public int getNumEventAmount() {
        return numEventAmount;
    }

    public void setNumEventAmount(int numEventAmount) {
        this.numEventAmount = numEventAmount;
    }

    public double getNumValue() {
        return numValue;
    }

    public void setNumValue(double numValue) {
        this.numValue = numValue;
    }

    public String getDesGrouping() {
        return desGrouping;
    }

    public void setDesGrouping(String desGrouping) {
        this.desGrouping = desGrouping;
    }

    public int getProduct() {
        return product;
    }

    public void setProduct(int product) {
        this.product = product;
    }
}
